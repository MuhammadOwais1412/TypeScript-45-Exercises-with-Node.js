// using if else-if statements
// version # 1
var alien_color = "green";
if (alien_color === "green") {
    console.log("Congratulation! you shot down green alien, you earned 5 points.");
}
else if (alien_color === "yellow") {
    console.log("Congratulation! you shot down yellow alien, you earned 10 points.");
}
else if (alien_color === "red") {
    console.log("Congratulation! you shot down red alien, you earned 15 points.");
}
